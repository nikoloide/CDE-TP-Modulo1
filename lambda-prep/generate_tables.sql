CREATE TABLE IF NOT EXISTS properties as (
    id VARCHAR(255) PRIMARY KEY,
    ad_type VARCHAR(55),
    start_date VARCHAR(55),
    end_date VARCHAR(55),
    created_on VARCHAR(55),
    nr_latitud FLOAT(2,6),
    nr_longitud FLOAT(2,6),
    pais VARCHAR(25),
    provincia VARCHAR(25),
    localidad VARCHAR(25),
    region_4 VARCHAR(25),
    region_5 VARCHAR(25),
    region_6 VARCHAR(25),
    rooms INT,
    bedrooms INT,
    bathrooms INT,
    surface_total FLOAT(10,6),
    surface_covered  FLOAT(10,6),
    price  c,
    currency VARCHAR(3),
    price_period VARCHAR(25),
    title VARCHAR(255),
    descripcion VARCHAR(255),
    property_type VARCHAR(55),
    operation_type VARCHAR(55),
    price_m2 FLOAT(10,6),
    x FLOAT(10,6),
    y FLOAT(10,6),
    nr_latitud_rad FLOAT(10,6),,
    nr_longitud_rad FLOAT(10,6),,
    property_type_med_price_m2 FLOAT(10,6),,
    property_type_med_price FLOAT(10,6),,
    property_type_med_surface_total FLOAT(10,6),
    Provincia VARCHAR(55),
    Localidad VARCHAR(55)
) ENGINE=INNODB;